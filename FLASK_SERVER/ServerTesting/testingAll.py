# Base URL for local testing
BASE_URL = "http://10.162.43.219:5000"

def save_response_to_json(response, filename):
    """Saves the response to a JSON file."""
    try:
        with open(filename, 'w') as f:
            json.dump(response.json(), f, indent=2)
        print(f"Response saved to {filename}")
    except Exception as e:
        print(f"Error saving response to JSON: {str(e)}")

def test_quiz_endpoint():
    """Test data for /quiz endpoint"""
    url = f"{BASE_URL}/quiz"

    # Test case 1: Basic recommendation
    payload1 = {
        "liked_tours": [10, 1],
        "disliked_tours": [102],
        "include_embeddings": True
    }

    # Test case 2: Empty preferences
    payload2 = {
        "liked_tours": [],
        "disliked_tours": [],
        "include_embeddings": False
    }

    # Test case 3: Only liked tours
    payload3 = {
        "liked_tours": [1, 102],
        "include_embeddings": True
    }

    print("\nTesting Quiz Endpoint:")
    for i, payload in enumerate([payload1, payload2, payload3], 1):
        print(f"\nTest case {i}:")
        print(f"Payload: {json.dumps(payload, indent=2)}")
        try:
            response = requests.post(url, json=payload)
            print(f"Status Code: {response.status_code}")

            # Save response to JSON file
            filename = f"quiz_test_case_{i}_response.json"
            save_response_to_json(response, filename)

        except Exception as e:
            print(f"Error: {str(e)}")

